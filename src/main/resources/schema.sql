/* 유저 계정 정보 테이블 */
CREATE TABLE TB_USER(
    ID VARCHAR(40) NOT NULL,
    PWD VARCHAR(250) NOT NULL,    
    
    CONSTRAINT PK_ID PRIMARY KEY (ID)
) DEFAULT CHARSET=utf8;

/* 키워드 검색 기록 테이블 */
CREATE TABLE TB_SEARCH_HISTORY(
    SEQ BIGINT NOT NULL AUTO_INCREMENT,
    ID VARCHAR(40) NOT NULL,
    REGDATE TIMESTAMP NOT NULL,
    KEYWORD VARCHAR(50) NOT NULL,
    
    CONSTRAINT PK_SEQ PRIMARY KEY (SEQ),
    INDEX IDX_ID (ID),
    INDEX IDX_REGDATE (REGDATE DESC)
) DEFAULT CHARSET=utf8;

/* 키워드 검색 횟수 테이블 */
CREATE TABLE TB_SEARCH_KEYWORD(
    KEYWORD VARCHAR(50) NOT NULL,
    SEARCH_COUNT BIGINT NOT NULL DEFAULT 1,
    
    CONSTRAINT UNQ_KEYWORD UNIQUE KEY (KEYWORD),
    INDEX IDX_SEARCH_COUNT (SEARCH_COUNT DESC)
) DEFAULT CHARSET=utf8;
